#include "hanio.h"
#include "extendio.h"

void main(void)
{
   char floppy[] =      /* 플로피 디스크의 이미지(16x16) */
   {
      0x00, 0x00, 0x00, 0x00, 0x7f, 0xfe, 0x44, 0x02,
      0x44, 0x02, 0x7f, 0xfe, 0x7e, 0x7e, 0x7c, 0x3e,
      0x7c, 0x2e, 0x7e, 0x7e, 0x7f, 0xfe, 0x7e, 0x7e,
      0x7e, 0x7e, 0x7f, 0xfe, 0x00, 0x00, 0x00, 0x00
   };
   char pencil[] =      /* 연필의 이미지(8x16) */
   {
      0x00, 0x3e, 0x22, 0x3e, 0x2a, 0x2a, 0x2a, 0x2a,
      0x2a, 0x2a, 0x2a, 0x3e, 0x22, 0x14, 0x08, 0x00
   };

   if(init_han() != OK_HAN) exit(1);

   xputs_han(0, 0, "플로피 디스크");
   zoomed_out_16(14, 0, floppy, FONT_HORIZ_DBL | FONT_VERT_DBL);
   xputs_han(20, 0, "연필");
   zoomed_out_8(25, 0, pencil, FONT_HORIZ_DBL);

   xputs_han(0, 2, "아무 키나 누르세요..");
   if(!getch()) getch();

   close_han();
}
